package acm.firstweek;

import java.util.Scanner;

public class A_AB1 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        while (scan.hasNext()) {
            int a = scan.nextInt();
            int b = scan.nextInt();
            System.out.println(a+b);
        }
    }

}
